#include <stdio.h>

int main()
{

    int num1, num2;

    scanf("%d %d" , &num1, &num2);
    printf ("a + b = %d\n", num1+num2);
    printf ("a - b = %d\n", num1-num2);
    printf ("a * b = %d\n", num1*num2);
    printf ("a / b = %d\n", num1/num2);
    printf ("a %% b = %d\n", num1%num2);

    return 0;
}
